namespace FPN\TheTVDB\Model;

abstract class AbstractModel
{
    public function fromArray(array $data)
    {
        foreach ($data as $key => $value) {
            if (property_exists($this, $key)) {
                $this->{$key} = $value;
            }
        }
    }
}
